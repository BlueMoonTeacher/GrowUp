export const getBehaviorExampleTraitGroups = (example: BehaviorRecordExample): string[] => {
  const searchable = [
    example.category,
    example.scene,
    example.title,
    example.context,
    example.content,
    example.followUp,
    example.traits.join(' '),
  ].join(' ');

  return BEHAVIOR_HELPER_TRAIT_GROUPS.filter(group => (
    traitGroupSignals[group].some(signal => searchable.includes(signal))
  ));
};

